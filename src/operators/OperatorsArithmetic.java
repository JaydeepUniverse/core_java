package operators;

public class OperatorsArithmetic {
    public static void main(String[] args) {
	int a = 1 + 1;
	int b = a * 3;
	int c = b / 4;
	int d = c - a;
	int e = -d;
	
	System.out.println(a + " " + b + " " + c + " " + d + " " + e);
	
	double aa = 1 + 1;
	double bb = aa * 3;
	double cc = bb / 4;
	double dd = cc - aa;
	double ee = -dd;
	
	System.out.println(aa + " " + bb + " " + cc + " " + dd + " " + ee);
    }
}
